''' 
    A program to show basic data structure such as
    Strings, Lists, Dictionaries, tuples, sets
'''

#string 
str = 'This is the 1st string'
print(str)

#Lists - unordered, can contain both numbers and strings
mylist = [1, 'two', 3, 4, 5]
print(mylist)

#Dictionaries - pair of key and value seperated by :
dict ={'key1' : 'value1', 'key2': 'value2'}
print(dict)

#tuples - ordered and unchangeable
tup = ('a', 'a', 'b', 'c')
print(tup)

#set - unordered and uniquesable
myset = set()
myset.add('a')
print(myset)